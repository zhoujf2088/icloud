package com.cloud.cases.vo;

import java.sql.Blob;

import com.cloud.cases.BaseVO;
/**
 * 申请人表实体对应VO
 * @author ZhouJF
 * @date 2016年5月31日 上午11:27:01
 */
public class ApplicantVO extends BaseVO {
	
	 private String cardID;		//身份证号码
    private String username;	//申请人姓名
    private String password;	//登陆密码
    private String origin;		//籍贯
    private int gender;			//性别   1=男  0=女
    private Blob cardphoto;		//身份证上的照片
    private String registeraddress;//户口所在地址
    private String nation;		// 民族
    private Blob fingerprint;	//指纹信息
    
    
	public String getCardID() {
		return cardID;
	}
	public void setCardID(String cardID) {
		this.cardID = cardID;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public Blob getCardphoto() {
		return cardphoto;
	}
	public void setCardphoto(Blob cardphoto) {
		this.cardphoto = cardphoto;
	}
	public String getRegisteraddress() {
		return registeraddress;
	}
	public void setRegisteraddress(String registeraddress) {
		this.registeraddress = registeraddress;
	}
	public String getNation() {
		return nation;
	}
	public void setNation(String nation) {
		this.nation = nation;
	}
	public Blob getFingerprint() {
		return fingerprint;
	}
	public void setFingerprint(Blob fingerprint) {
		this.fingerprint = fingerprint;
	}
    
    

}
